class FlightData:
   def __init__(self, price, org_city, org_airport, dest_city, dest_airport, out_date, return_date):
       self.price = price
       self.origin_city = org_city
       self.origin_airport = org_airport
       self.destination_city = dest_city
       self.destination_airport = dest_airport
       self.out_date = out_date
       self.return_date = return_date